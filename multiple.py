#Check if a number is a multiple of 3 and a multiple of 7
#use modules and conditionals to do this

x = 21
y = 16

if x % 3 == 0 and x % 7 == 0:
  print("x is divisible by 3 and 7")
else:
  print("x is not divisible by 3 and/or 7")

if y % 3 == 0 and y % 7 == 0:
  print("y is divisible by 3 and 7")
else:
  print("y is not divisible by 3 and/or 7")